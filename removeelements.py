class Solution:
    # @param A : list of integers
    # @param B : integer
    # @return an integer
    def removeElement(self, A, B):
        
        end = len(A) - 1
        ind = 0
        
        while ind <= end:
            if A[ind] == B:
                for i in range(ind, end):
                    A[i] = A[i + 1]
                end -= 1
            if A[ind] != B:
                ind += 1
            
        return A[:end + 1]
    

